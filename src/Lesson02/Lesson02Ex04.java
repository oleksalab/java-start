package Lesson02;

public class Lesson02Ex04
{
    public static void main(String[] args)
    {
        char ch;

        ch = 's';         // - symbol
        ch = '\u0073';    // - unicode
        ch = 0x73;        // - ascii (hex)
        ch = 115;         // - ascii (dec)

        System.out.println("Char : " + ch);

    }
}
